-- Episode 3 setup: a tiny todo model on top of episode 1's `issues` table.
--
-- The three tables are the canonical app from docs/building-app-pipelines.md: `lists` group
-- `todos`, and `list_members` records who may see which list. It is small enough that you can
-- predict every shape, count, and move-in by reading the seed — exactly as `issues` is in
-- episode 1. Apply it by hand (the episode walks you through it); it is NOT part of the initial
-- seed, so `docker compose down -v && docker compose up` still resets to episode 1's lone
-- `issues` table.
--
-- The engine sets REPLICA IDENTITY FULL on every table it introspects, so these need nothing
-- special for retractions to carry their prior tuple. `id` is GENERATED BY DEFAULT (not ALWAYS)
-- so the explicit-id seed rows below coexist with auto-assigned ids on later inserts.

CREATE TABLE lists (
  id    bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name  text   NOT NULL
);

CREATE TABLE todos (
  id        bigint  GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  list_id   bigint  NOT NULL,
  done      boolean NOT NULL DEFAULT false,
  title     text    NOT NULL,
  assignee  text    NOT NULL
);

CREATE TABLE list_members (
  id       bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  list_id  bigint NOT NULL,
  user_id  text   NOT NULL
);

